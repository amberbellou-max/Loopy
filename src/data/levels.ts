const food = (
  foodType: FoodSpawnRule["foodType"],
  x: number,
  y: number,
  count: number,
  spreadX: number,
  spreadY: number,
  movement: FoodSpawnRule["movement"],
): FoodSpawnRule => ({
  foodType,
  x,
  y,
  count,
  spreadX,
  spreadY,
  movement,
});

const predator = (
  x: number,
  y: number,
  count: number,
  spacing: number,
  speed: number,
  shootIntervalMs: number,
  projectileSpeed: number,
): PredatorSpawnRule => ({
  x,
  y,
  count,
  spacing,
  speed,
  shootIntervalMs,
  projectileSpeed,
});

const wormhole = (
  x: number,
  y: number,
  pullRadius: number,
  pullStrength: number,
  shootIntervalMs: number,
  projectileSpeed: number,
): WormholeRule => ({
  x,
  y,
  pullRadius,
  pullStrength,
  shootIntervalMs,
  projectileSpeed,
});

const hazard = (
  type: HazardRule["type"],
  x: number,
  y: number,
  width: number,
  height: number,
  damage: number,
): HazardRule => ({
  type,
  x,
  y,
  width,
  height,
  damage,
});

const seedGhost = (
  triggerX: number,
  x: number,
  y: number,
  rewardSeeds: SeedFountainGhostRule["rewardSeeds"],
  visibleMs = 2000,
): SeedFountainGhostRule => ({
  triggerX,
  x,
  y,
  rewardSeeds,
  visibleMs,
});

const spinArena = (
  triggerX: number,
  x: number,
  y: number,
  radius: number,
  orbitRadius: number,
  orbitSpeed: number,
  seedSpinRate: number,
  predatorSwirl: number,
): SpinArenaRule => ({
  triggerX,
  x,
  y,
  radius,
  orbitRadius,
  orbitSpeed,
  seedSpinRate,
  predatorSwirl,
});

